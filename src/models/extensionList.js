    /**
     * Builds a regular expression string from the selected extensions.
     * @param {string[]} extensions The string array of extensions.
     */
    static constructRegex(extensions) {
        if (extensions.length === 0) return "";

        const updatedExtensions = [];
        for (let extension of extensions) {
            if (extension[0] === "{" && extension[extension.length - 1] === "}") {
                updatedExtensions.push(...ExtensionList[ExtensionList.items[extension.substring(1, extension.length - 1)]]);
            } else {
                extension = extension.trim().toLowerCase();
                if (extension[0] === '.') {
                    extension = extension.substring(1);
                }

                updatedExtensions.push(extension)
            }
        }

        return `\\.(${updatedExtensions.join("|")})$`;
    }
